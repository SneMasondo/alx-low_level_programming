#ifndef MAIN_H
#define MAIN_H

/**
 * struct dog - structure that defines a dog
 * @name: name of the dog
 * @age: age of the dog
 * @owner: name of the dog's owner
 *
 * Description: This structure stores the information of a dog, including its 
 * name, age, and the name of its owner.
 */
struct dog {
	char *name;
	float age;
	char *owner;
};

#endif /* MAIN_H */

